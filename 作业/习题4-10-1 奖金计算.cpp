#include <stdio.h>

int main() {
	double l, f;
	scanf("%lf", &l);
	if (l <= 100000) {
		f = l * 0.1;
	}
	if (100000 < l && l <= 200000) {
		f = 100000 * 0.1 + (l - 100000) * 0.075;
	}
	if (200000 < l && l <= 400000) {
		f = 100000 * 0.1 + 100000 * 0.075 + (l - 200000) * 0.05;
	}
	if (400000 < l && l <= 600000) {
		f = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (l - 400000) * 0.03;
	}
	if (600000 < l && l <= 1000000) {
		f = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + (l - 600000) * 0.015;
	}
	if (l > 1000000) {
		f = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + 4000000 * 0.015 + (l - 10000000) * 0.01;
	}
	printf("%.2f\n", f);
	return 0;
}